""" Ejercicio 6
Cree un programa que pida los lados de un rectángulo y calcule la
área. """

print("Hola!" + "\nEn este programa calcularemos el área de un rectángulo!")
s1 = float(input("Ingrese la longitud en centimetros del primer lado del rectángulo: "))
s2 = float(input("Ingrese la longitud en centimetros del segundo lado del rectángulo: "))
print("\nEl área del rectángulo es de:", s1 * s2, "cm^2. Bien hecho!")
""" Ejercicio 10
Cree un programa en el cual pida al usuario el nombre y la edad y muestre
por consola la edad que tentra dentro de 10 años. """

print("Hola!" + "\nEn este programa usted será capaz de saber cuantos años tendra en 10 años, que locura!")

user_name = input("Ingrese su nombre: ")
user_age = int(input("Ingrese su edad: "))

print("Usted, " + user_name + " tendrá en 10 años: " + str(user_age + 10) + " años. Apure que el tiempo corre ^^")
# Cree un programa que calcule el área de un triángulo. Base x altura sobre 2.

print("Hola!" + "\nEn este programa calcularemos el área de un triángulo!")
base = float(input("Ingrese la longitud en centimetros de la base: "))
height = float(input("Ingrese la longitud en centimetros de la altura: "))
print("\nEl área del triángulo es de:", (base*height)/2, "cm^2. Bien hecho!")